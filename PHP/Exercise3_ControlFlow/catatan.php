<?php
    // PENGULANGAN
    // for
    // while
    // do.. while
    // foreach | (note: pengulangan khusus array)
    
    // -FOR-
    for ($i=1; $i < 5; $i++) { 
        echo $i . "Hello World! <br>";
    }

    // -WHILE-
    $a = 0;
    while ($a <= 10) {
        echo "Hellow World! <br>";
        $a++;
    }

    // -DO.. WHILE-
    $x = 0;
    do {
        echo "Hello World? <br>";
        $x++;
    } while($x < 5);
?>